var a = 10;
var b = 20;
var exp, mod;
document.write("A= ", a, " and B= ", b, "<br>");
// assign with operator and output
exp = a ** b;
document.write("A ** B = ", exp, "<br>");
mod = a % b;
document.write("A % B = ", mod, "<br>");
a++; ++a;
document.write("A= ", a, "<br>");
--b; b--;
document.write("B= ", b, "<br>");
