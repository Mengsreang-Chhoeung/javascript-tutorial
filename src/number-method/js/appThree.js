// initial value
var number = 123.456;

// using toFixed()
document.write("Number " + number.toFixed(0) + "<br>");
document.write("Number " + number.toFixed(2) + "<br>");
document.write("Number " + number.toFixed(4) + "<br><br>");

// using toPrecision()
document.write("Number " + number.toPrecision() + "<br>");
document.write("Number " + number.toPrecision(2) + "<br>");
document.write("Number " + number.toPrecision(4) + "<br>");
