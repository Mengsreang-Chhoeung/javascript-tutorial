// initial value
var fruits = ["Banana", "Orange", "Apple", "Mango"];

// using toString() method
document.write("Fruits: " + fruits.toString() + "<br><br>");

// using join() method
document.write("Fruits: " + fruits.join(" ") + "<br>");
document.write("Fruits: " + fruits.join("-") + "<br>");
document.write("Fruits: " + fruits.join(" ~ ") + "<br>");
