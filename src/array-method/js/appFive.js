// initial value
var fruitOne = ["Banana", "Orange", "Apple", "Mango"];
var fruitTwo = ["Coconut", "Cherry"];

// using concat() method
document.write("Concatenate Fruits: " + fruitOne.concat(fruitTwo) + "<br><br>");

// using slice() method
document.write("Slice Fruits: " + fruitOne.slice(1) + "<br>");
document.write("Slice Fruits: " + fruitOne.slice(1, 3) + "<br><br>");
