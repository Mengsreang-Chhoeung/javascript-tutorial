// initial value
var txt = "Apple, Banana, Kiwi";

// using substring method
document.write("Substring of Txt: " + txt.substring(7, 13) + "<br>");
document.write("Substring of Txt: " + txt.substring(15) + "<br>");

// using substr method
document.write("Substr of Txt: " + txt.substr(7, 6) + "<br>");
document.write("Substr of Txt: " + txt.substr(15) + "<br>");
document.write("Substr of Txt: " + txt.substr(-12, 6) + "<br>");
document.write("Substr of Txt: " + txt.substr(-4) + "<br>");
