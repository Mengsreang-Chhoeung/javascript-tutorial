// initial value
var txt = "Apple, Orange, Coconut";

// using charAt method
document.write("CharAt of Txt: " + txt.charAt(7) + "<br>");
// or using this []
document.write("Character of Txt: " + txt[7] + "<br>");

// using charCodeAt method
document.write("CharCodeAt of Txt: " + txt.charCodeAt(7) + "<br>");

// using split method
document.write("Slit comma(,) sign of Txt: " + txt.split(",")[0] + "<br>");
document.write("Slit comma(,) sign of Txt: " + txt.split(",")[2] + "<br>");
