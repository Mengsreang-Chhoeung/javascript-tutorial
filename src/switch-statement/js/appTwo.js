// initial value
var character = 'a';
// check condition with switch statement
switch (character) {
  case 'A':
  case 'a':
    document.write("Character A");
    break;
  case 'B':
  case 'b':
    document.write("Character B");
    break;
  case 'C':
  case 'c':
    document.write("Character C");
    break;
  default:
   	document.write("Invalid character");
   	break;
}
